// Flatten the tree for table display
export function flattenMenus(items: Menu[], parentName = ''): (Menu & { parentName: string; level: number })[] {
  let result: (Menu & { parentName: string; level: number })[] = []
  for (const item of items) {
    const level = parentName ? parentName.split(' / ').length : 0
    result.push({ ...item, parentName, level })
    if (item.children) {
      result = result.concat(flattenMenus(item.children, parentName ? `${parentName} / ${item.name}` : item.name))
    }
  }
  return result
}

export const flatMenus = flattenMenus(menus)
